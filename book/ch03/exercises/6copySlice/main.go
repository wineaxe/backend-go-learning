package main

import (
	"fmt"
)

func main() {

	// ПРИМЕР 1: Копирование из меньшего среза в больший
	a6 := []int{-10, 1, 2, 3, 4, 5} // срез-приёмник (destination), длина 6

	a4 := []int{-1, -2, -3, -4} // срез-источник (source), длина 4

	fmt.Println("a6:", a6) // до копирования: [-10 1 2 3 4 5]

	fmt.Println("a4:", a4) // [-1 -2 -3 -4]

	// copy(dst, src) копирует элементы из src в dst
	// Копируется минимум из len(dst) и len(src) элементов
	// Здесь: len(a6)=6, len(a4)=4 → скопируется 4 элемента
	copy(a6, a4)

	fmt.Println("a6:", a6) // после: [-1 -2 -3 -4 4 5] — первые 4 элемента заменены, остальные 2 не тронуты

	fmt.Println("a4:", a4) // [-1 -2 -3 -4] — источник не изменяется

	fmt.Println()

	// ПРИМЕР 2: Копирование из большего среза в меньший
	b6 := []int{-10, 1, 2, 3, 4, 5} // срез-источник, длина 6

	b4 := []int{-1, -2, -3, 4} // срез-приёмник, длина 4

	fmt.Println("b6:", b6) // [-10 1 2 3 4 5]

	fmt.Println("b4:", b4) // [-1 -2 -3 4]

	// Копируем из b6 (len=6) в b4 (len=4)
	// Копируется минимум: min(6, 4) = 4 элемента
	copy(b4, b6)

	fmt.Println("b6:", b6) // [-10 1 2 3 4 5] — источник не изменяется

	fmt.Println("b4:", b4) // [-10 1 2 3] — все 4 элемента заменены, остальные из b6 не поместились

	fmt.Println()

	// ПРИМЕР 3: Копирование из массива в срез (через срез массива)
	array4 := [4]int{4, -4, 4, -4} // массив из 4 элементов

	s6 := []int{1, 1, -1, -1, 5, -5} // срез-приёмник, длина 6

	// array4[0:] создаёт срез из всего массива (от индекса 0 до конца)
	// Копируем из среза массива в s6: min(4, 6) = 4 элемента
	copy(s6, array4[0:])

	fmt.Println("array4:", array4[0:]) // [4 -4 4 -4] — срез массива (массив не изменён)

	fmt.Println("s6:", s6) // [4 -4 4 -4 5 -5] — первые 4 элемента заменены

	fmt.Println()

	// ПРИМЕР 4: Копирование из среза в массив (через срез массива)
	array5 := [5]int{5, -5, 5, -5, 5} // массив из 5 элементов

	s7 := []int{7, 7, -7, -7, 7, -7, 7} // срез-источник, длина 7

	// array5[0:] создаёт срез из всего массива
	// Копируем из s7 в срез массива: min(5, 7) = 5 элементов
	copy(array5[0:], s7)

	fmt.Println("array5:", array5) // [7 7 -7 -7 7] — массив изменён через срез

	fmt.Println("s7:", s7) // [7 7 -7 -7 7 -7 7] — источник не изменяется

}
