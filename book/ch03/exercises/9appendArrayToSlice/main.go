package main

import (
	"fmt"
)

func main() {

	// Создаём срез из 3 элементов
	s := []int{1, 2, 3}

	// Создаём массив из 3 элементов
	a := [3]int{4, 5, 6}

	// Создаём срез из массива: a[:] — срез, который "смотрит" на весь массив
	ref := a[:]

	fmt.Println("Existing array:\t", ref) // [4 5 6]

	// append(s, ref...) — добавляем все элементы из ref в конец s
	// Троеточие (...) "распаковывает" срез ref, добавляя каждый элемент отдельно
	// Результат присваиваем в новую переменную t — s не изменяется
	t := append(s, ref...)

	fmt.Println("New slice:\t", t) // [1 2 3 4 5 6] — новый срез
	// s остался прежним: [1 2 3]

	// Теперь изменяем существующий срез s: добавляем элементы из ref
	// s = append(...) — результат присваиваем обратно в s
	s = append(s, ref...)

	fmt.Println("Existing slice:\t", s) // [1 2 3 4 5 6] — s изменён

	// Добавляем сам срез к себе: s = append(s, s...)
	// Троеточие распаковывает s и добавляет все его элементы в конец
	s = append(s, s...)

	fmt.Println("s+s:\t\t", s) // [1 2 3 4 5 6 1 2 3 4 5 6] — s удвоен
}
