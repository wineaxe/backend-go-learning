package main

import (
	"fmt"
	"time" // пакет для работы со временем
)

func main() {

	// Unix timestamp (эпоха): количество секунд с 1 января 1970 года 00:00:00 UTC
	// time.Now() — текущее время, .Unix() — преобразование в Unix timestamp
	fmt.Println("Epoch time:", time.Now().Unix())
	// Вывод: Epoch time: 1739123456 (пример, число зависит от текущего времени)

	// Сохраняем текущее время в переменную t (тип time.Time)
	t := time.Now()

	// Выводим время в двух форматах:
	// t — стандартный формат Go (например: 2026-02-09 18:30:45.123456 +0300 MSK)
	// t.Format(time.RFC3339) — формат RFC3339 (например: 2026-02-09T18:30:45+03:00)
	fmt.Println(t, t.Format(time.RFC3339))

	// Извлекаем отдельные компоненты времени:
	// t.Weekday() — день недели (Monday, Tuesday, ...)
	// t.Day() — день месяца (1-31)
	// t.Month() — месяц (January, February, ...)
	// t.Year() — год (например: 2026)
	fmt.Println(t.Weekday(), t.Day(), t.Month(), t.Year())
	// Вывод: Monday 9 February 2026 (пример)

	// time.Sleep приостанавливает выполнение программы на указанное время
	// time.Second — константа, равная 1 секунде
	time.Sleep(time.Second) // ждём 1 секунду

	// Получаем новое время после паузы
	t1 := time.Now()

	// t1.Sub(t) — вычисляет разницу между двумя моментами времени
	// Возвращает time.Duration (например: 1.000123456s)
	fmt.Println("Time difference:", t1.Sub(t))
	// Вывод: Time difference: 1.000123456s (примерно 1 секунда + небольшая погрешность)

	// Форматирование времени по пользовательскому шаблону
	// "01 January 2006" — специальный формат Go:
	// - 01 — день месяца (или 02 для двузначного)
	// - January — полное название месяца
	// - 2006 — год (Go использует фиксированную дату 2006-01-02 15:04:05 как эталон)
	formatT := t.Format("01 January 2006")

	fmt.Println(formatT) // Вывод: 09 February 2026 (пример)

	// Загрузка временной зоны (timezone)
	// time.LoadLocation("Europe/Paris") — загружает информацию о часовом поясе Парижа
	// Второе значение (_) — ошибка, которую мы игнорируем (в реальном коде нужно обработать)
	loc, _ := time.LoadLocation("Europe/Paris")

	// t.In(loc) — преобразует время t в указанный часовой пояс
	londonTime := t.In(loc)

	fmt.Println("Paris:", londonTime) // Вывод: Paris: 2026-02-09 17:30:45 +0100 CET
	// Время будет отличаться от исходного на разницу часовых поясов
}
