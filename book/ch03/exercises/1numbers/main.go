package main // объявление пакета: точка входа в программу

import (
	"fmt" // пакет для форматированного ввода-вывода
)

func main() {
	c1 := 12 + 1i                      // комплексное число: 12 — действительная часть, 1 — мнимая (тип complex128)
	c2 := complex(5, 7)                // комплексное число через функцию: 5 + 7i (тип complex128)
	fmt.Printf("Type of c1: %T\n", c1) // выводим тип переменной c1
	fmt.Printf("Type of c2: %T\n", c2) // выводим тип переменной c2

	var c3 complex64 = complex64(c1 + c2) // сумма c1 и c2, приведённая к типу complex64 (32-битное комплексное)
	fmt.Printf("c3: %v\n", c3)            // вывод значения c3 в формате по умолчанию
	fmt.Printf("Type of c3: %T\n", c3)    // выводим тип c3

	cZero := c3 - c3             // вычитание: c3 − c3 = 0 (нулевое комплексное число)
	fmt.Println("cZero:", cZero) // печать значения cZero

	x := 12                          // целое число (тип int)
	k := 5                           // целое число (тип int)
	fmt.Println(x)                   // печатает букву "x", а не значение переменной x
	fmt.Printf("Type of x: %T\n", x) // выводим тип переменной x

	div := x / k            // целочисленное деление: 12 / 5 = 2 (дробная часть отбрасывается)
	fmt.Println("div", div) // печать результата деления

	var m, n float64           // объявление двух переменных типа float64 (числа с плавающей точкой)
	m = 1.223                  // присваивание значения m
	fmt.Println("m, n:", m, n) // n остаётся нулём (значение по умолчанию для float64)

	y := 4 / 2.3         // деление с плавающей точкой: 4 и 2.3 — литералы, результат float64
	fmt.Println("y:", y) // печать y

	divFloat := float64(x) / float64(k)            // явное приведение к float64 — получаем дробное деление 12/5 = 2.4
	fmt.Println("divFloat", divFloat)              // печать результата
	fmt.Printf("Type of divFloat: %T\n", divFloat) // тип результата — float64
}
